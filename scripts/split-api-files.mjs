/*
 * Splits each namespace-grouped apis/*.ts file into per-endpoint apis/<stem>.ts
 * files so each file imports exactly one schema. Required for Option B per-
 * endpoint lazy dispatch; otherwise invoking `indices create` loads ~60 schemas.
 *
 * Idempotent-ish: refuses to overwrite if target file already exists.
 */
import { readdir, readFile, writeFile, unlink } from 'node:fs/promises'
import { join } from 'node:path'
import { fileURLToPath } from 'node:url'

const APIS_DIR = join(fileURLToPath(import.meta.url), '..', '..', 'src', 'es', 'apis')

const HEADER = `/*
 * Copyright Elasticsearch B.V. and contributors
 * SPDX-License-Identifier: Apache-2.0
 */

/*
 * This file was automatically generated by elastic/elastic-client-generator-js
 * and then split per-endpoint by scripts/split-api-files.mjs.
 * DO NOT MODIFY IT BY HAND.
 */
`

function endpointStem (name, namespace) {
  const raw = namespace == null ? name : `${namespace}.${name}`
  return raw.replace(/\./g, '_').replace(/-/g, '_')
}

function toCamelCase (stem) {
  return stem.replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase())
}

async function splitFile (fileStem) {
  const filepath = join(APIS_DIR, `${fileStem}.ts`)
  const src = await readFile(filepath, 'utf8')

  // Parse all Passthrough definitions so we can reattach them
  const passthroughs = new Map()
  for (const m of src.matchAll(/^const (\w+Passthrough) = (\w+Request)\.extend\(\{([\s\S]*?)^\}\)$/gm)) {
    passthroughs.set(m[1], { base: m[2], body: m[3] })
  }

  // Parse each API definition block
  const defRe = /^\s\{\s*$([\s\S]*?)^\s\},/gm
  const fieldRe = /^\s*(name|namespace|description|method|path|input|responseType|bodyFormat):\s*([^\n,]*?),?\s*$/gm

  const emitted = []
  for (const m of src.matchAll(defRe)) {
    const body = m[1]
    const raw = {}
    for (const fm of body.matchAll(fieldRe)) raw[fm[1]] = fm[2].trim()
    if (!raw.name) continue
    const name = JSON.parse(raw.name.replace(/^'(.*)'$/, '"$1"'))
    const namespace = raw.namespace != null ? JSON.parse(raw.namespace.replace(/^'(.*)'$/, '"$1"')) : null
    const stem = endpointStem(name, namespace)
    const exportName = `${toCamelCase(stem)}Apis`

    // Figure out schema imports
    const inputName = raw.input  // e.g. `IndicesCreateRequest` or `BulkRequestPassthrough`
    const imports = []
    let extraConst = null
    if (inputName != null) {
      if (passthroughs.has(inputName)) {
        const pt = passthroughs.get(inputName)
        imports.push(`import { z } from 'zod'`)
        imports.push(`import { ${pt.base} } from './schemas/${stem}.ts'`)
        extraConst = `const ${inputName} = ${pt.base}.extend({${pt.body}})`
      } else {
        imports.push(`import { ${inputName} } from './schemas/${stem}.ts'`)
      }
    }
    imports.push(`import type { EsApiDefinition } from '../types.ts'`)

    const defLines = ['{']
    defLines.push(`  name: ${JSON.stringify(name)},`)
    if (namespace != null) defLines.push(`  namespace: ${JSON.stringify(namespace)},`)
    defLines.push(`  description: ${JSON.parse(raw.description.replace(/^'(.*)'$/, '"$1"')) != null ? JSON.stringify(JSON.parse(raw.description.replace(/^'(.*)'$/, '"$1"'))) : raw.description},`)
    defLines.push(`  method: ${raw.method},`)
    defLines.push(`  path: ${raw.path},`)
    if (inputName != null) defLines.push(`  input: ${inputName},`)
    if (raw.responseType != null) defLines.push(`  responseType: ${raw.responseType},`)
    if (raw.bodyFormat != null) defLines.push(`  bodyFormat: ${raw.bodyFormat},`)
    defLines.push('}')

    const out = HEADER + '\n' + imports.join('\n') + '\n\n' +
      (extraConst != null ? extraConst + '\n\n' : '') +
      `export const ${exportName}: EsApiDefinition[] = [\n ${defLines.join('\n ')},\n]\n`

    const outPath = join(APIS_DIR, `${stem}.ts`)
    await writeFile(outPath, out)
    emitted.push({ stem, name, namespace })
  }
  return emitted
}

const files = (await readdir(APIS_DIR))
  .filter(f => f.endsWith('.ts') && !f.startsWith('.'))
  .map(f => f.replace(/\.ts$/, ''))

let totalEmitted = 0
const filesToRemove = []
for (const fileStem of files) {
  const emitted = await splitFile(fileStem)
  totalEmitted += emitted.length
  // Remove the original namespace file unless it already matched an endpoint stem
  const sameAsEndpoint = emitted.length === 1 && emitted[0].stem === fileStem
  if (!sameAsEndpoint) {
    filesToRemove.push(join(APIS_DIR, `${fileStem}.ts`))
  }
}

for (const f of filesToRemove) {
  await unlink(f)
}

console.log(`Split -> ${totalEmitted} per-endpoint files emitted, ${filesToRemove.length} namespace files removed.`)
